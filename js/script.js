function updateLeft(duration) 
{
	if (user.spent<duration*60) user.left = (duration*60) - user.spent + 1; // update the time left using test duration and time spent
	else if (user.spent>=duration*60||user.left<=0) { var al = alert('The time for your test has elapsed.'); return;} // inform user that time has elapsed and end
	user.left -= 0.5; // register a loss of 0.5s from the time left
	convertAndShowTime(user.left,'timeLeft'); // convert seconds left to time and display on timeLeft
	
	if (user.left>=600 && user.left<((duration*60)/2)) {
		if (notified[3]==false) {
			var confirmation = confirm("Half of your time has been exhausted...");
			if (confirmation) notified[3] = true;
		}
	} else if (user.left>=300 && user.left<600) {
		if (notified[2]==false) {
			var confirmation = confirm("Less than 10 minutes more!");
			if (confirmation) notified[2] = true;
		}
	} else if (user.left>=60 && user.left<300) {
		if (notified[1]==false) {
			var confirmation = confirm("Less than 5 minutes more!");
			if (confirmation) notified[1] = true;
		}
	} else if (user.left>=0.5 && user.left<60) {
		if (notified[0]==false) {
			document.getElementById('stopBtn').style.disabled = true;
			var confirmation = confirm("Done!");
			if (confirmation) notified[0] = true;
		}
	} else if (user.left<0.5) {
		document.getElementById('stopBtn').style.disabled = true;
		stopExam(); // alert user to pens up and disable start and stop buttons
	}
}

function convertAndShowTime(secs,id)
{
	if (secs>60) {
		var timenow = convertToTime(secs);
		if (timenow[2]<1) document.getElementById(id).innerHTML = timenow[1] + ":" + formatUnit(timenow[0]);
		else document.getElementById(id).innerHTML = timenow[2] + ":" + formatUnit(timenow[1]) + ":" + formatUnit(timenow[0]);
    	} else document.getElementById(id).innerHTML = formatUnit(Math.floor(secs));
}

function convertToTime(secs) 
{
	var timenow = [];
	timenow[0] = Math.floor(secs%60);
	timenow[1] = (Math.floor(secs/60))%60;
	timenow[2] = Math.floor(secs/3600);	
	return timenow;	
}

function formatUnit(i) 
{
  if (i < 10) {i = "0" + i};  // add zero in front of numbers < 10
  return i;
}

