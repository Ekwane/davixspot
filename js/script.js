function checkPerformance(questioncount)
{
	this.responses = getResponses();
	user.totalanswered = 0; this.responses.forEach(addToTotalAnswered); // get all responses and add the count to user.totalanswered
	var attemptedRatio = (user.totalanswered/questioncount)*100;
	var passRatio = (user.correctanswers/user.totalanswered)*100;
	var percentScore = (user.correctanswers/questioncount)*100;
	if (!(attemptedRatio>0)) attemptedRatio = 0;
	if (!(passRatio>0)) passRatio = 0;
	if (!(percentScore>0)) percentScore = 0;
	if (convertToTime(user.spent)[2]>0) document.getElementById('resultSheet').innerHTML = "<br>~Performance~<br>Attempted:\t"+user.totalanswered+"<br>In:\t"+convertToTime(user.spent)[2]+"hrs"+convertToTime(user.spent)[1]+"mins"+convertToTime(user.spent)[0]+"secs<br>Covered:\t"+attemptedRatio+"%<br>Coverage:\t"+(attemptedRatio/(user.duration/user.spent))*100+"%<br>Rate:\t"+(attemptedRatio/user.spent)*100+"%<br>Passed:\t"+Math.floor(passRatio)+"%<br>Efficiency:\t"+Math.floor(passRatio*(user.duration/user.spent))/100+"%<br>Score:\t"+percentScore+"%";
	else if (convertToTime(user.spent)[1]>0) document.getElementById('resultSheet').innerHTML = "<br>~Performance~<br>Attempted:\t"+user.totalanswered+"<br>In:\t"+convertToTime(user.spent)[1]+"mins"+convertToTime(user.spent)[0]+"secs<br>Covered:\t"+attemptedRatio+"%<br>Coverage:\t"+(attemptedRatio/(user.duration/user.spent))*100+"%<br>Rate:\t"+(attemptedRatio/user.spent)*100+"%<br>Passed:\t"+Math.floor(passRatio)+"%<br>Efficiency:\t"+Math.floor(passRatio*(user.duration/user.spent))/100+"%<br>Score:\t"+percentScore+"%";
	else if (convertToTime(user.spent)[0]>0) document.getElementById('resultSheet').innerHTML = "<br>~Performance~<br>Attempted:\t"+user.totalanswered+"<br>In:\t"+convertToTime(user.spent)[0]+"secs<br>Covered:\t"+attemptedRatio+"%<br>Coverage:\t"+(attemptedRatio/(user.duration/user.spent))*100+"%<br>Rate:\t"+(attemptedRatio/user.spent)*100+"%<br>Passed:\t"+Math.floor(passRatio)+"%<br>Efficiency:\t"+Math.floor(passRatio*(user.duration/user.spent))/100+"%<br>Score:\t"+percentScore+"%";
	if (pensUp==false) setTimeout(monitorPerformance, 60000); // monitor the user's test performance every minute
	
	return percentScore;
}

