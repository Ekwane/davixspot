function checkPerformance(questioncount)
 {
 	document.getElementById("resultSheet").style.display = "block";
 	this.responses = getResponses();
 	user.totalanswered = 0; this.responses.forEach(addToTotalAnswered); // get all responses and add the count to user.totalanswered
 	var attemptedRatio = Math.round((user.totalanswered/questioncount)*100);
 	var passRatio = Math.round((user.correctanswers/user.totalanswered)*100);
 	var percentScore = Math.round((user.correctanswers/questioncount)*100);
 	if (!(attemptedRatio>0)) attemptedRatio = 0;
 	if (!(passRatio>0)) passRatio = 0;
 	if (!(percentScore>0)) percentScore = 0;
 	document.getElementById("subPer").innerHTML = document.getElementById("pageHeader").innerHTML;
 	document.getElementById("nq").innerHTML = questioncount;
 	document.getElementById("aq").innerHTML = user.totalanswered;
 	document.getElementById("tspq").innerHTML = timeSpentInMinutes(user.totalanswered) + " second(s)/question";
 	document.getElementById("caoaq").innerHTML = user.correctanswers  + "/" + user.totalanswered;
 	document.getElementById("psfaq").innerHTML = passRatio  + "%";
 	document.getElementById("ocaoaq").innerHTML =  user.correctanswers+ "/" +questioncount ;
 	document.getElementById("ops").innerHTML = percentScore + "%";
 }

