function convertAndShowTime(secs,id)
{
	if (secs>60) {
		var timenow = convertToTime(secs);
		if (timenow[2]<1) document.getElementById(id).innerHTML = timenow[1] + ":" + formatUnit(timenow[0]);
		else document.getElementById(id).innerHTML = timenow[2] + ":" + formatUnit(timenow[1]) + ":" + formatUnit(timenow[0]);
    	}
    else document.getElementById(id).innerHTML = formatUnit(Math.floor(secs));
}

function convertToTime(secs) 
{
	var timenow = [];
	timenow[0] = Math.floor(secs%60);
	timenow[1] = (Math.floor(secs/60))%60;
	timenow[2] = Math.floor(secs/3600);	
	return timenow;
}

function formatUnit(i) 
 {
   if (i < 10) {i = "0" + i};  // add zero in front of numbers < 10
   return i;
 }

